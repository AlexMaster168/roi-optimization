    def load_custom_profile(self, org_name, profile_data):
        if "protection_costs" in profile_data:
            profile_data["protection_costs"] = {
                int(k): v for k, v in profile_data["protection_costs"].items()
            }
        self.org_profiles[org_name] = profile_data

    def remove_custom_profile(self, org_name):
        if org_name in self.org_profiles:
            del self.org_profiles[org_name]

    def generate_history(self, org_type, years=5, noise_level=0.005):
        profile = self.org_profiles[org_type]
        base_probs = np.array(profile["base_probs"])
        data = []
        for year in range(years):
            trend = np.linspace(-0.05, 0, years)[year]
            noise = np.random.normal(0, noise_level, size=len(base_probs))
            probs = np.clip(base_probs + trend + noise, 0.01, 0.99)
            total_risk = np.sum(probs * np.array(profile["losses"]))
            data.append({
                "year": year,
                "probs": probs.tolist(),
                "total_risk": total_risk,
                "incident_count": int(np.sum(probs) * 1000)
            })
        return pd.DataFrame(data)

    def get_profile(self, org_type):
        return self.org_profiles[org_type]

    def get_all_org_types(self):
        return list(self.org_profiles.keys())
